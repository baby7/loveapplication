package com.love.myapplication.entity;

/**
 * 慧语简报
 */
public class News {
    private String newsWrap;
    private String historyWrap;
    private String phraseWrap;
    private String wisdomWord;
    private String progressBar;
    private String progressBarValue;

    public String getNewsWrap() {
        return newsWrap;
    }

    public void setNewsWrap(String newsWrap) {
        this.newsWrap = newsWrap;
    }

    public String getHistoryWrap() {
        return historyWrap;
    }

    public void setHistoryWrap(String historyWrap) {
        this.historyWrap = historyWrap;
    }

    public String getPhraseWrap() {
        return phraseWrap;
    }

    public void setPhraseWrap(String phraseWrap) {
        this.phraseWrap = phraseWrap;
    }

    public String getWisdomWord() {
        return wisdomWord;
    }

    public void setWisdomWord(String wisdomWord) {
        this.wisdomWord = wisdomWord;
    }

    public String getProgressBar() {
        return progressBar;
    }

    public void setProgressBar(String progressBar) {
        this.progressBar = progressBar;
    }

    public String getProgressBarValue() {
        return progressBarValue;
    }

    public void setProgressBarValue(String progressBarValue) {
        this.progressBarValue = progressBarValue;
    }
}
